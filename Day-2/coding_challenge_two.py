current_age = int(input("What is you current age? "))
remaining_years = 90 - current_age
number_of_days_remaining = 365 * remaining_years
number_of_weeks_remaining = 52 * remaining_years
number_of_months_remaining = 12 * remaining_years
print(f"You have {number_of_days_remaining} days {number_of_weeks_remaining} weeks, and  {number_of_months_remaining} months left.")